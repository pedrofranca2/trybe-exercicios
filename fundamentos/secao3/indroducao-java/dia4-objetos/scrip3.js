let animal = {
    nome: "Jade",
    idade: 10,
    comida: "petisco",
}

// Como retornar um array com as chaves
let keys = Object.keys(animal);
console.log(keys);

// Como retornar um array com os valores
let values = Object.values(animal);
console.log(values);

// Como retornar um array com as entradas
let entries = Object.entries(animal);
console.log(entries);

